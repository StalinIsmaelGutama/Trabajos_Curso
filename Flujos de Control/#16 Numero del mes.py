"""
16. Programa para imprimir el nombre del mes correspondiente a un número del 1 al 12:
"""
mes = int(input("Ingrese un numero del 1 al 12 para saber el mes del año :"))

if mes == 1:
    print(f"El numero {mes}, corresponde al mes de Enero.")
elif mes == 2:
    print(f"El numero {mes}, corresponde al mes de Febrero.")
elif mes == 3:
    print(f"El numero {mes}, corresponde al mes de Marzo.")
elif mes == 4:
    print(f"El numero {mes}, corresponde al mes de Abril.")
elif mes == 5:
    print(f"El numero {mes}, corresponde al mes de Mayo.")
elif mes == 6:
    print(f"El numero {mes}, corresponde al mes de Junio.")
elif mes == 7:
    print(f"El numero {mes}, corresponde al mes de Julio.")
elif mes == 8:
    print(f"El numero {mes}, correspondee al mes de Agosto.")
elif mes == 9:
    print(f"El numero {mes}, corresponde al mes de Septiembre.")
elif mes == 10:
    print(f"El numero {mes}, corresponde al mes de Octubre.")
elif mes == 11:
    print(f"El numero {mes}, corresponde al mes de Noviembre.")
elif mes == 12:
    print(f"El numero {mes}, corresponde al mes de Diciembre.")
else:
    print(f"El numero {mes}, no corresponde a ningun mes ")




    
    

    

